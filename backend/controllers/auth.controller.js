const { validationResult } = require('express-validator');
const jwt = require('jsonwebtoken');
const User = require('../models/User.model');

// Generate JWT Token with userId and role
const generateToken = (id, role) => {
  return jwt.sign(
    { 
      id,
      role 
    }, 
    process.env.JWT_SECRET, 
    {
      expiresIn: process.env.JWT_EXPIRE || '30d'
    }
  );
};

// @desc    Register new user
// @route   POST /api/auth/register
// @access  Public
exports.register = async (req, res) => {
  try {
    // Validate request
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        status: 'error',
        errors: errors.array()
      });
    }

    const { name, email, password, role } = req.body;

    // Check if user already exists
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(400).json({
        status: 'error',
        message: 'User already exists with this email'
      });
    }

    // HARDCODED: JheevaShankar is ALWAYS MANAGER
    let assignedRole;
    if (email === 'jheeva123@gmail.com') {
      assignedRole = 'MANAGER';
    } else {
      // Determine role: First user becomes MANAGER, others become TEAM_MEMBER
      const userCount = await User.countDocuments();
      assignedRole = userCount === 0 ? 'MANAGER' : (role || 'TEAM_MEMBER');
    }

    // Create user
    const user = await User.create({
      name,
      email,
      password,
      role: assignedRole
    });

    // Generate token with userId and role
    const token = generateToken(user._id, user.role);

    res.status(201).json({
      status: 'success',
      data: {
        user: {
          id: user._id,
          name: user.name,
          email: user.email,
          role: user.role
        },
        token
      }
    });
  } catch (error) {
    res.status(500).json({
      status: 'error',
      message: error.message
    });
  }
};

// @desc    Login user
// @route   POST /api/auth/login
// @access  Public
exports.login = async (req, res) => {
  try {
    // Validate request
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        status: 'error',
        errors: errors.array()
      });
    }

    const { email, password } = req.body;

    // Find user and include password
    const user = await User.findOne({ email }).select('+password');

    if (!user) {
      return res.status(401).json({
        status: 'error',
        message: 'Invalid email or password'
      });
    }

    // Check password
    const isMatch = await user.comparePassword(password);

    if (!isMatch) {
      return res.status(401).json({
        status: 'error',
        message: 'Invalid email or password'
      });
    }

    // HARDCODED: Ensure jheeva123@gmail.com is ALWAYS MANAGER
    if (user.email === 'jheeva123@gmail.com' && user.role !== 'MANAGER') {
      user.role = 'MANAGER';
      await user.save();
    }

    // Generate token with userId and role
    const token = generateToken(user._id, user.role);

    res.status(200).json({
      status: 'success',
      data: {
        user: {
          id: user._id,
          name: user.name,
          email: user.email,
          role: user.role
        },
        token
      }
    });
  } catch (error) {
    res.status(500).json({
      status: 'error',
      message: error.message
    });
  }
};

// @desc    Get current user
